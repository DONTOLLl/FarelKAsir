    private void loginAction() {
    String username = text_username.getText();
    String password = new String(text_password.getPassword());

    try (Connection con = Koneksi.configDB()) {
        String sql = "SELECT * FROM tbl_user WHERE username=? AND password=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, password);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Login berhasil, selamat datang " + username + "!");
            // TODO: pindah ke menu utama
             new menu_masakan().setVisible(true);
             this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Username atau password salah!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        ex.printStackTrace();
    }
}
